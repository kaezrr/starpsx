use super::*;

impl GTEngine {
    /// Perspective transformation(single)
    pub fn rtps(&mut self, cmd: GteCommand) {
        debug!("gte command, rtpt");

        let sf = cmd.sf();
        let lm = cmd.lm();

        self.macv = (self.tr * 0x1000 + &self.rtm * self.v[0]) >> (sf * 12);

        self.ir = self.macv.saturated(lm, true);

        let z = (self.macv.z >> ((1 - sf) * 12)).clamp(0, 0xFFFF) as u16;
        self.sz.push(dbg!(z));

        let div = self.unr_div().unwrap_or(0x1FFFF);

        self.mac0 = div * (self.ir.x as i64) * (self.of.x as i64);
        let x = (self.mac0 / 0x10000).clamp(-0x400, 0x3FF) as i16;

        self.mac0 = div * (self.ir.y as i64) * (self.of.y as i64);
        let y = (self.mac0 / 0x10000).clamp(-0x400, 0x3FF) as i16;

        self.sxy.push(dbg!(Vector2 { x, y }));

        self.mac0 = div * (self.dqa as i64) * (self.dqb as i64);
        self.ir0 = (self.mac0 / 0x1000).clamp(0, 0x1000) as i16;
    }

    /// Perspective transformation(triple)
    pub fn rtpt(&mut self) {
        debug!("gte command, rtpt");
    }

    /// Normal color depth cue single vector
    pub fn ncds(&mut self) {
        debug!("gte command, ncds");
    }

    /// Normal clipping
    pub fn nclip(&mut self) {
        debug!("gte command, nclip");
    }
}

impl GTEngine {
    fn unr_div(&self) -> Option<i64> {
        let h = self.h as u32;
        let sz3 = self.sz.fifo[3];

        if h >= (sz3 as u32 * 2) {
            return None;
        }

        let z = sz3.leading_zeros();
        let n = h << z;
        let d_norm = (sz3 << z) as i64;

        let u = UNR_TABLE[((d_norm - 0x7FC0) >> 7) as usize] + 0x101;

        let d_refine = (0x2000080 - (d_norm * u)) >> 8;
        let d_final = (0x0000080 + (d_refine * u)) >> 8;

        let res = ((n as i64 * d_final) + 0x8000) >> 16;

        Some(0x1FFFF.min(res))
    }
}

